package dto;

public class HistoryObject {

	private int ID;
	private double X;
	private double Y;
	private String inquiryDate;
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public double getX() {
		return X;
	}
	public void setX(double x) {
		X = x;
	}
	public double getY() {
		return Y;
	}
	public void setY(double y) {
		Y = y;
	}
	public String getInquiryDate() {
		return inquiryDate;
	}
	public void setInquiryDate(String date) {
		this.inquiryDate = date;
	}
	
	
}
