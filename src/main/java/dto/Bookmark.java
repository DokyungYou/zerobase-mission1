package dto;

public class Bookmark {

	private int groupID;
	private int bookmarkID;
	private String bookmarkName;
	private String wifiName;
	private String wifiNum;
	private String registerDTTM;
	private String distance;
	
	
	
	public int getGroupID() {
		return groupID;
	}
	public void setGroupID(int groupID) {
		this.groupID = groupID;
	}
	public int getBookmarkID() {
		return bookmarkID;
	}
	public void setBookmarkID(int bookmarkID) {
		this.bookmarkID = bookmarkID;
	}
	public String getBookmarkName() {
		return bookmarkName;
	}
	public void setBookmarkName(String bookmarkName) {
		this.bookmarkName = bookmarkName;
	}
	public String getWifiName() {
		return wifiName;
	}
	public void setWifiName(String wifiName) {
		this.wifiName = wifiName;
	}
	public String getWifiNum() {
		return wifiNum;
	}
	public void setWifiNum(String wifiNum) {
		this.wifiNum = wifiNum;
	}
	public String getRegisterDTTM() {
		return registerDTTM;
	}
	public void setRegisterDTTM(String registerDTTM) {
		this.registerDTTM = registerDTTM;
	}
	public String getDistance() {
		return distance;
	}
	public void setDistance(String distance) {
		this.distance = distance;
	}
	
}
